#include "libft.h"
#include <unistd.h>

char	*ft_strchr(const char *s, int c)
{
	unsigned int i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)(&s[i]));
		i++;
	}
	if (s[i] == '\0' && (char)c == '\0')
		return ((char *)(&s[i]));
	return (0);
}
